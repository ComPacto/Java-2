    private void cadastrar(){
        Retangulo objRetangulo = new Retangulo();
        
        objRetangulo.setAltura(Double.parseDouble(jtAltura.getText()));
        objRetangulo.setLargura(Double.parseDouble(jtLargura.getText()));
        
        JOptionPane.showMessageDialog(
                this,
                "Dados: " + objRetangulo.toString(),
                "Relatório",
                JOptionPane.PLAIN_MESSAGE);
    }
    
    private void limpar(){
        jtAltura.setText(null);
        jtLargura.setText(null);
        jtAltura.grabFocus();
    }
