    private void jtAlturaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtAlturaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            jtLargura.grabFocus();
        }
    }//GEN-LAST:event_jtAlturaKeyPressed

